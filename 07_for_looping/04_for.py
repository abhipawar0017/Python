#range function :
# syntax :
# range(starting_nos , last_number , step)

# starting_nos : from where you wnt to start it  , by default it will be zero
# Last_nos : that is end of ur sequence(End-1)

print(list(range(1 ,101)))

print(list(range(1 ,101, 2))) # increment by 1

print(list(range(1 ,101, 3))) # increment by 2

print(list(range(101 ,1, -2))) # increment by 2

print(list(range(-4 ,4))) #Negative Integer

print(list(range(0,1)))

print(list(range(1,1)))

