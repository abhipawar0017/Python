## Question
## +,-,*,/ operations

n = int(input("Enter the 1st number : "))
m = int(input("Enter the 2nd number : "))
o = input("Enter sign from +,-,x,/ : ")

if(o == "+"):
  print(f"Addition : {n+m}",)
elif(o == "-"):
  print(f"Addition : {n-m}",)
elif(o == "x"):
  print(f"Addition : {n*m}",)
elif(o == "/"):
  print(f"Addition : {n/m}",)
else:
  print("Entered wrong symbol.")
